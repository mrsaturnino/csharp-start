//O foreach é uma estrutura de repetição muito utilizada com arrays e listas.

string[] hobbies = {"Viajar", "Ler", "Plantas", "Corrida", "Games"};

foreach (string hobby in hobbies) //Leia desta forma: Para cada hobby na array hobbies, execute o seguinte bloco de código;
{
    Console.WriteLine(hobby);
}
//Desta forma, o foreach percorre cada item da array separadamente!
//Atente-se ao "para cada ..."

